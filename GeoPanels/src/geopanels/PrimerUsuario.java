    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
       // Tomando los datos del formulario y pasandoles a los atributos de la clase.

       if (txtNombre.getText().equals("") || txtApellido.getText().equals("") || txtTelefono.getText().equals("") || txtCorreo.getText().equals("") || txtUsuario.getText().equals("") || txtDireccion.getText().equals("") || txtNacimiento.getText().equals("") || txtPassword.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
        }else{
           
           // Patrón para validar el email.
	   Pattern pattern = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@" + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");           
           
           // El email a validar.
           String email = txtCorreo.getText();
           
           Matcher mather = pattern.matcher(email);
           
           if (mather.find() == true){
           
                txtPassword.setEnabled(true);
                rbdMasculino.setEnabled(true);
                rbdFemenino.setEnabled(true);
                String Valor1;//ESTADO.
                String Valor2;//TIPO.
                String Valor3;//ESTABLECIMIENTO.

                Valor1 = cbxEstado.getItemAt(cbxEstado.getSelectedIndex()).getRespuesta();
                Valor2 = cbxTipo.getItemAt(cbxTipo.getSelectedIndex()).getRespuesta();
                Valor3 = cbxEstablecimiento.getItemAt(cbxEstablecimiento.getSelectedIndex()).getRespuesta();

                obj.setNombre(txtNombre.getText());
                obj.setApellido(txtApellido.getText());
                obj.setCorreo(txtCorreo.getText());
                obj.setNombre_usuario(txtUsuario.getText());
                obj.setDireccion(txtDireccion.getText());
                obj.setFecha_nacimiento(txtNacimiento.getText());
                obj.setTelefono(txtTelefono.getText());
                obj.setPerfil(save);

                //ENCRIPTANDO CONTRASEÑA.

                //Llamo a las clases.
                Encriptacion encriptar = new Encriptacion();

                String secretKey = "Ejemplo";
                String CadenaAEncriptar = txtPassword.getText();
                obj.setContra(encriptar.encriptar(secretKey, CadenaAEncriptar));

                if (rbdMasculino.isSelected()) {

                    obj.setGenero("Masculino");

                     //ComboBox           
                     obj.setId_estado(Integer.parseInt(Valor1));
                     obj.setId_tipo_empleado(Integer.parseInt(Valor2));
                     obj.setId_establecimiento(Integer.parseInt(Valor3));

                if (obj.getPerfil()!= null) {
                    funciones.CargarLogo(obj.getPerfil(), lblPerfil);

                        if (obj.guardarProyecto()) {
                        JOptionPane.showMessageDialog(null, "Datos Guardados");
                        txtNombre.setText(null);
                        txtApellido.setText(null);
                        txtTelefono.setText(null);
                        txtCorreo.setText(null);
                        txtUsuario.setText(null);
                        txtDireccion.setText(null);
                        txtNacimiento.setText(null);
                        txtPassword.setText(null);
                        buttonGroup1.clearSelection();
                        
                        //Dando paso al siguiente formulario.
                        Seguridad se = new Seguridad();
                        se.setVisible(true);
                        this.dispose();

                        }else{
                           JOptionPane.showMessageDialog(null, "Error al guardar datos"); 
                            txtNombre.setText(null);
                            txtApellido.setText(null);
                            txtTelefono.setText(null);
                            txtCorreo.setText(null);
                            txtUsuario.setText(null);
                            txtDireccion.setText(null);
                            txtNacimiento.setText(null);
                            txtPassword.setText(null);
                            buttonGroup1.clearSelection();
                        }
                    }else{

                        JOptionPane.showMessageDialog(null, "Es obligatorio cologar una fotografia reciente del empleado a agregar.");
                    }

               }else if (rbdFemenino.isSelected()) {

                   obj.setGenero("Femenino");

                    //ComboBox           
                    obj.setId_estado(Integer.parseInt(Valor1));
                    obj.setId_tipo_empleado(Integer.parseInt(Valor2));
                    obj.setId_establecimiento(Integer.parseInt(Valor3));

                    if (obj.getPerfil()!= null) {
                        funciones.CargarLogo(obj.getPerfil(), lblPerfil);

                        if (obj.guardarProyecto()) {
                        JOptionPane.showMessageDialog(null, "Datos Guardados");
                        txtNombre.setText(null);
                        txtApellido.setText(null);
                        txtTelefono.setText(null);
                        txtCorreo.setText(null);
                        txtUsuario.setText(null);
                        txtDireccion.setText(null);
                        txtNacimiento.setText(null);
                        txtPassword.setText(null);
                        buttonGroup1.clearSelection();
                        
                        //Dando paso al siguiente formulario.
                        Seguridad se = new Seguridad();
                        se.setVisible(true);
                        this.dispose();

                        }else{
                           JOptionPane.showMessageDialog(null, "Error al guardar datos"); 
                            txtNombre.setText(null);
                            txtApellido.setText(null);
                            txtTelefono.setText(null);
                            txtCorreo.setText(null);
                            txtUsuario.setText(null);
                            txtDireccion.setText(null);
                            txtNacimiento.setText(null);
                            txtPassword.setText(null);
                            buttonGroup1.clearSelection();
                        }
                }else{

                    JOptionPane.showMessageDialog(null, "Es obligatorio cologar una fotografia reciente del empleado a agregar.");
                }

               }else{

                    JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
               }
           }else{
           
               JOptionPane.showMessageDialog(null, "El correo ingresado no corresponde a una escritura valida, favor corregirlo", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
           }

       }
    }//GEN-LAST:event_btnAñadirActionPerformed

