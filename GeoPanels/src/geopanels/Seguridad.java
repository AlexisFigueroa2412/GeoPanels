    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtRespuesta1.getText().equals("") || txtRespuesta2.getText().equals("") || txtRespuesta3.getText().equals("") || txtRespuesta4.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
        }else{
        
            String Valor1;
            String Valor2;
            String Valor3;
            String Valor4;

            ClasePreguntas obj = new ClasePreguntas();

            Valor1 = cmbPregunta1.getItemAt(cmbPregunta1.getSelectedIndex()).getRespuesta();
            Valor2 = cmbPregunta2.getItemAt(cmbPregunta2.getSelectedIndex()).getRespuesta();
            Valor3 = cmbPregunta3.getItemAt(cmbPregunta3.getSelectedIndex()).getRespuesta();
            Valor4 = cmbPregunta4.getItemAt(cmbPregunta4.getSelectedIndex()).getRespuesta();

            obj.setId_usuario(Integer.parseInt(txtID.getText()));
            obj.setId_pregunta1(Integer.parseInt(Valor1));
            obj.setRespuesta1(txtRespuesta1.getText());
            obj.setId_pregunta2(Integer.parseInt(Valor2));
            obj.setRespuesta2(txtRespuesta2.getText());
            obj.setId_pregunta3(Integer.parseInt(Valor3));
            obj.setRespuesta3(txtRespuesta3.getText());
            obj.setId_pregunta4(Integer.parseInt(Valor4));
            obj.setRespuesta4(txtRespuesta4.getText());

            if (obj.guardarProyecto()) {
                JOptionPane.showMessageDialog(null, "Datos Guardados");
                Login lg = new Login();
                lg.setVisible(true);
                this.dispose();

            }else{

               JOptionPane.showMessageDialog(null, "Error al guardar datos"); 
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

