   public void cargarContrato(){

        DefaultTableModel modelo = (DefaultTableModel) TablaContrato.getModel();
        modelo.setRowCount(0);

        //Realizando consulta.
        result = Clases.Conexion.Consulta("Select c.id_contrato, c.mensualidad, c.garantia, c.fecha_inicio, c.fecha_fin, c.rendimiento, m.tipo_mantenimiento, o.produccion, c.recargo, n.nombre_cliente, t.tipo_contrato, c.codigo from Contrato c, Cliente n, Tipo_contrato t, Tipo_mantenimiento m, Produccion o where c.id_cliente = n.id_cliente AND c.id_tipo_contrato = t.id_tipo_contrato AND c.costo = m.id_tipo_mantenimiento AND c.produccion = o.id_produccion");

        try{

            while(result.next()){

                //Agregando datos a la tabla.
                Vector datos = new Vector();
                datos.add(result.getInt(1));
                datos.add(result.getInt(2));
                datos.add(result.getString(3));
                datos.add(result.getString(4));
                datos.add(result.getString(5));
                datos.add(result.getString(6));
                datos.add(result.getString(7));
                datos.add(result.getString(8));
                datos.add(result.getInt(9));
                datos.add(result.getString(10));
                datos.add(result.getString(11));
                datos.add(result.getInt(12));
                modelo.addRow(datos);
                TablaContrato.setModel(modelo);

                //Ocultando la columna 0.
                TablaContrato.getColumnModel().getColumn(0).setMaxWidth(0);
                TablaContrato.getColumnModel().getColumn(0).setMinWidth(0);
                TablaContrato.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
                TablaContrato.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
