    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        Valor1 = txtCliente.getText();
        Valor2 = txtTipoMan.getText();
        Valor3 = txtProduccion.getText();

        if (txtMensualidad.getText().equals("") || txtGarantia.getText().equals("") || txtRecargo.getText().equals("") || txtCliente.getText().equals("") ||
            txtRend.getText().equals("") || txtTipoMan.getText().equals("") || txtFechaIn.getText().equals("") || txtFechaFin.getText().equals("") ||
            txtFechaIn.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String CMB;//MUNICIPIO.

            CMB = cbxTipoContrato.getItemAt(cbxTipoContrato.getSelectedIndex()).getRespuesta();

            ClaseContrato obj = new ClaseContrato();

            obj.setmensualidad(Double.parseDouble(txtMensualidad.getText()));
            obj.setgarantia(txtGarantia.getText());
            obj.setfecha_inicio(txtFechaIn.getText());
            obj.setfecha_fin(txtFechaFin.getText());
            obj.setrendimiento(txtRend.getText());
            obj.setrecargo(Double.parseDouble(txtRecargo.getText()));
            obj.setcodigo(Integer.parseInt(txtCodigo.getText()));

            ClaseCombo_Contrato mostrar = new ClaseCombo_Contrato();

            int costo = (mostrar.CargarCosto(Valor2));
            int produccion = (mostrar.CargarProduccion(Valor3));
            int cliente = (mostrar.CargarCliente(Valor1));

            obj.setcosto(costo);
            obj.setproduccion(produccion);
            obj.setid_cliente(cliente);
            obj.setid_tipo_contrato(Integer.parseInt(CMB));

            if (obj.guardarContrato()) {
                JOptionPane.showMessageDialog(null, "Datos Guardados");
                cargarContrato();
                LimpiarCampos();
            }else{
                JOptionPane.showMessageDialog(null, "Error al guardar datos");
            }
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

