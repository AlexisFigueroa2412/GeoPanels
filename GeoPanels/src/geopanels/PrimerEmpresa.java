    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
       // Tomando los datos del formulario y pasandoles a los atributos de la clase.

       if (txtEmpresa.getText().equals("") || txtGiro.getText().equals("") || txtRazon.getText().equals("") || txtCorreo.getText().equals("") || txtPatronal.getText().equals("") || txtDireccion.getText().equals("") || txtRepresentante.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
       }else{
       
           // Patrón para validar el email.
	   Pattern pattern = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@" + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");           
           
           // El email a validar.
           String email = txtCorreo.getText();
           
           Matcher mather = pattern.matcher(email);
           
           if (mather.find() == true){
           
                Obj.setNombre_empresa(txtEmpresa.getText());
                Obj.setGiro_comercial(txtGiro.getText());
                Obj.setRazon_social(txtRazon.getText());
                Obj.setCorreo(txtCorreo.getText());
                Obj.setRegistro_patronal(txtPatronal.getText());
                Obj.setDireccion(txtDireccion.getText());
                Obj.setRepresentante(txtRepresentante.getText());
                Obj.setLogo(save);

                if (Obj.getLogo()!= null) {
                    funciones.CargarLogo(Obj.getLogo(), lblImagen);
                }
                if (Obj.GuardarEmpresa()) 
                {
                    JOptionPane.showMessageDialog(this, "Datos Ingresados Corretamente");
                    PrimerEstablecimiento pre = new PrimerEstablecimiento();
                    pre.setVisible(true);
                    this.dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Error al modificar la información");
                }
                
           }else{
           
               JOptionPane.showMessageDialog(null, "El correo ingresado no corresponde a una escritura valida, favor corregirlo", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
           }
            
       }
        
    }//GEN-LAST:event_btnSiguienteActionPerformed

