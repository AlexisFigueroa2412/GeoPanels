    public void Busqueda(){

        String valor = txtCodigo.getText();
        DefaultTableModel modelo = (DefaultTableModel) TablaContrato.getModel();
        modelo.setRowCount(0);

        //Realizando consulta.
        result = Clases.Conexion.Consulta("Select id_contrato, fecha_inicio, fecha_fin, codigo From Contrato Where codigo = '"+valor+"'");
        try{

            while(result.next()){

                //Agregando datos a la tabla.
                Vector datos = new Vector();
                datos.add(result.getInt(1));
                datos.add(result.getString(2));
                datos.add(result.getString(3));
                datos.add(result.getInt(4));

                modelo.addRow(datos);
                TablaContrato.setModel(modelo);

                //Ocultando la columna 0.
                TablaContrato.getColumnModel().getColumn(0).setMaxWidth(0);
                TablaContrato.getColumnModel().getColumn(0).setMinWidth(0);
                TablaContrato.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
                TablaContrato.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);

            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
