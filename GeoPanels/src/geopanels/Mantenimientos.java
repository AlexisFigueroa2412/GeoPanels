    private void TablaMantenimientoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMantenimientoMouseClicked
        int col = TablaMantenimiento.getSelectedRow();
        txtID.setText(TablaMantenimiento.getModel().getValueAt(col, 0).toString());
        txtFecha.setText(TablaMantenimiento.getModel().getValueAt(col, 1).toString());
        txtDescripcion.setText(TablaMantenimiento.getModel().getValueAt(col, 2).toString());
        txtCodigo.setText(TablaMantenimiento.getModel().getValueAt(col, 3).toString());
        txtUser.setText(TablaMantenimiento.getModel().getValueAt(col, 5).toString());

        //Capturando los datos de los combobox.
        String valor = TablaMantenimiento.getModel().getValueAt(col, 4).toString();
        ClaseCombo_MantenimientoGen Nose = new ClaseCombo_MantenimientoGen();
        cmbTipoMan.setSelectedIndex(Nose.CargarTipoMan(valor));
        String valor2 = TablaMantenimiento.getModel().getValueAt(col, 6).toString();
        cmbEstado.setSelectedIndex(Nose.CargarEstado(valor2));
    }//GEN-LAST:event_TablaMantenimientoMouseClicked

