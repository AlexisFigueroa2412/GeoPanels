public void cargarValores() {
     
       DefaultTableModel modelo = (DefaultTableModel) TablaMantenimiento.getModel();
       modelo.setRowCount(0);
       
       result = Clases.Conexion.Consulta("Select m.id_mantenimiento, m.fecha, m.descripcion, c.codigo, t.costo, d.nombre_usuario, o.estado From Mantenimiento_Generado m, Contrato c, Tipo_mantenimiento t, Usuario d, Estado_mantenimiento o Where m.id_contrato = c.id_contrato AND m.id_tipo = t.id_tipo_mantenimiento AND m.id_usuario = d.id_usuario AND m.id_estado_mantenimiento = o.id_estado");
       try{
       
           while(result.next()){
             
               Vector valor = new Vector();
                valor.add(result.getInt(1));
                valor.add(result.getString(2));
                valor.add(result.getString(3));
                valor.add(result.getInt(4));
                valor.add(result.getInt(5));
                valor.add(result.getString(6));
                valor.add(result.getString(7));
               modelo.addRow(valor);
               TablaMantenimiento.setModel(modelo);
               
               
               TablaMantenimiento.getColumnModel().getColumn(0).setMaxWidth(0);
               TablaMantenimiento.getColumnModel().getColumn(0).setMinWidth(0);
               TablaMantenimiento.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
               TablaMantenimiento.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
           }
       }
       catch(Exception e){
       
           System.out.println(e.getMessage());
       }
          }
