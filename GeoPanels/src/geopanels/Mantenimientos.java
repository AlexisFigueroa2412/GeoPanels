             ClaseCombo_MantenimientoGen mostrar = new ClaseCombo_MantenimientoGen();
             
             int Codigo = (mostrar.CargarCodigo(Valor2));
            int Usuario = (mostrar.CargarUsuario(Valor1));
            
            obj.setid_usuario(Usuario);
            obj.setid_contrato(Codigo);
            obj.setid_estado_mantenimiento(Integer.parseInt(CMB2));
            obj.setid_tipo(Integer.parseInt(CMB));
            
