    public void Busqueda(){

        String valor = txtNombre.getText();
        DefaultTableModel modelo = (DefaultTableModel) TablaClientes.getModel();
        modelo.setRowCount(0);

        //Realizando consulta.
        result = Clases.Conexion.Consulta("Select id_cliente, nombre_cliente, apellido_cliente, dui, numero_telefonico From Cliente Where nombre_cliente = '"+valor+"'");

        try{

            while(result.next()){

                //Agregando datos a la tabla.
                Vector datos = new Vector();
                datos.add(result.getInt(1));
                datos.add(result.getString(2));
                datos.add(result.getString(3));
                datos.add(result.getString(4));
                datos.add(result.getString(5));
    
                modelo.addRow(datos);
                TablaClientes.setModel(modelo);

                //Ocultando la columna 0.
                TablaClientes.getColumnModel().getColumn(0).setMaxWidth(0);
                TablaClientes.getColumnModel().getColumn(0).setMinWidth(0);
                TablaClientes.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
                TablaClientes.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);

            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
