    private void btncomprobar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncomprobar1ActionPerformed
        Clase_Preguntas obj = new Clase_Preguntas();
        obj.setnombre_usuario(txtusuario.getText());        
        if (obj.consultarUsuario()) 
        {
            JOptionPane.showMessageDialog(null, "Usuario encontrado");
            txtid.setText(obj.getusuario().toString());
            cmb1.setEnabled(true);
            cmb2.setEnabled(true);
            cmb3.setEnabled(true);
            cmb4.setEnabled(true);
            txt1.setEnabled(true);
            txt2.setEnabled(true);
            txt3.setEnabled(true);
            txt4.setEnabled(true);
            txtusuario.setEnabled(false);
            btncomprobar1.setEnabled(false);  
            btncomprobar.setEnabled(true);
            txtusuario.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Usuario incorrecto");
        }
    }//GEN-LAST:event_btncomprobar1ActionPerformed

    private void btncomprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncomprobarActionPerformed
        Clase_Preguntas obj = new Clase_Preguntas();
        obj.setrespuesta1(txt1.getText());
        obj.setrespuesta2(txt2.getText());
        obj.setrespuesta3(txt3.getText());
        obj.setrespuesta4(txt4.getText());
        
        if (obj.consultarRespuestas()) 
        {
            JOptionPane.showMessageDialog(null, "Respuestas Correctas");
            txtcontra.setEnabled(true);
            txtcontra2.setEnabled(true);
            btnaceptar.setEnabled(true);
            txt1.setEnabled(false);
            txt2.setEnabled(false);
            txt3.setEnabled(false);
            txt4.setEnabled(false);
            cmb1.setEnabled(false);
            cmb2.setEnabled(false);
            cmb3.setEnabled(false);
            cmb4.setEnabled(false);
            txt1.setText("");
            txt2.setText("");
            txt3.setText("");
            txt4.setText("");
        }
    }//GEN-LAST:event_btncomprobarActionPerformed

    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        Clase_Preguntas obj = new Clase_Preguntas();
        if (txtcontra.getText().equals(txtcontra2.getText())) 
        {
            ClaseLogin L = new ClaseLogin();
            JOptionPane.showMessageDialog(null, "Contraseña reestablecida");
            obj.setcontraseña(L.EncriptarPalabra(txtcontra2.getText()));
            obj.setusuario(Integer.parseInt(txtid.getText()));            
            obj.reestablecerContraseña();
            txtcontra.setEnabled(false);
            txtcontra2.setEnabled(false);
            btnaceptar.setEnabled(false);
            Login con = new Login();
            con.setVisible(true);
            this.dispose();
        }        
        else
        {
            JOptionPane.showMessageDialog(null, "Las contraseñas deben coincidir");
        }
    }//GEN-LAST:event_btnaceptarActionPerformed

