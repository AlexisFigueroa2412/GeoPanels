    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        Valor1 = txtProducto.getText();
        if (txtProducto.getText().equals("") || txtCantidad.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);

        }
        else{
            String CMB;//Proveedor
            String Esta;
            CMB = cmbProveedor.getItemAt(cmbProveedor.getSelectedIndex()).getRespuesta();
            Esta = cmbEstablecimiento.getItemAt(cmbEstablecimiento.getSelectedIndex()).getRespuesta();
            ClaseCarrito_Compra obj = new ClaseCarrito_Compra();
            obj.setcantidad(Integer.parseInt(txtCantidad.getText()));
            obj.setid_carrito(Integer.parseInt(txtIdCarro.getText()));

            ClaseCombo_CarritoCompra mostrar = new ClaseCombo_CarritoCompra();
            int Producto = (mostrar.CargarProducto(Valor1));
            obj.setid_producto(Producto);
            obj.setid_proveedor(Integer.parseInt(CMB));
            obj.setId_establecimiento(Integer.parseInt(Esta));
            

            if (obj.guardarCarrito()) {
                JOptionPane.showMessageDialog(null, "Datos Guardados");
                Llenar_Tabla();

            }else{
                JOptionPane.showMessageDialog(null, "Error al guardar datos");
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

