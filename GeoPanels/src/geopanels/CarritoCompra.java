 private void Llenar_Tabla()
    {
        ClaseCarrito_Compra Obj = new ClaseCarrito_Compra();
        Obj.setcarrito(txtCarrito.getText());
        DefaultTableModel llenar = (DefaultTableModel) TablaCarrito.getModel();
        llenar.setRowCount(0);
        retorno = Obj.Mostrar_carro();
        try
        {
            while(retorno.next())
            {
                Vector v = new Vector();
                v.add(retorno.getInt(1));
                v.add(retorno.getString(2));
                v.add(retorno.getString(3));
                v.add(retorno.getString(4));
                v.add(retorno.getInt(5));
                v.add(retorno.getString(6));
                llenar.addRow(v);
                TablaCarrito.setModel(llenar);
            }
        }
        catch(Exception e)
        {
            System.out.println(e.toString());
        }      
    }
public void Conseguir_Carrito(){
        car.setcarrito(txtCarrito.getText());
        retorno = car.Obtener_carrito();
                    
            try{
                while(retorno.next())
                {                    
                    txtIdCarro.setText(retorno.getString(1));                           
                    if (!txtIdCarro.getText().isEmpty()) {
                        //JOptionPane.showMessageDialog(this, "Correlativo del carrito" + retorno.getString(1));
                        txtCantidad.setEnabled(true);
                        btnProducto.setEnabled(true);
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Correlativo del carrito" + retorno.getString(1));
                    }
                }
            }
                catch(Exception e){
                JOptionPane.showMessageDialog(this, "No se pudo obtener el id perteneciente al carrito" + e);
            }
    } public void Limpiar_tabla(){
        DefaultTableModel tb = (DefaultTableModel) TablaCarrito.getModel();
        int a = TablaCarrito.getRowCount()-1;
        for (int i = a; i >= 0; i--) {          
        tb.removeRow(tb.getRowCount()-1);
        }
    }    
