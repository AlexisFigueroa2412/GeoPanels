    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Clase_Rendimiento obj = new Clase_Rendimiento();
        int combo = obj.MetodoCombo(cmbproducto.getItemAt(cmbproducto.getSelectedIndex()));
        obj.setproduccion(txtproduccion.getText());
        obj.setnombre_producto(combo);
        
        if (obj.guardarProyecto()) 
        {
            JOptionPane.showMessageDialog(this, "Datos guardados");
            cargarValores();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Error al guardar datos");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

