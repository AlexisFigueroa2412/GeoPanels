    public void Obtener_Tarjeta(){
        fac.setPan(Pan);
        fac.setCVV(CVV);
        try{
            retorno = fac.Obtener_Tarjeta();
            while(retorno.next())
            {
                ID = retorno.getInt(1);
                
            }
        }
        catch(SQLException e){
            System.out.println(e);
        }
    }
    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        try{
            fac.setNombreTarjeta(Nombre);
            fac.setApellidoTarjeta(Apellido);
            fac.setPan(Pan);
            fac.setCVV(CVV);
            fac.setVencimiento(vencimiento);
//            JOptionPane.showMessageDialog(null, fac.getNombreTarjeta() + " "+ fac.getApellidoTarjeta()+" "+fac.getPan()
//                    +" "+fac.getCVV()+" "+fac.getVencimiento(), "Error en el ingreso", JOptionPane.INFORMATION_MESSAGE);
                if (fac.Agregar_Tarjeta()) {
                    JOptionPane.showMessageDialog(this, "Datos guardados");
                    Obtener_Tarjeta();
                    Pagos pg = new Pagos();
                    pg.setVisible(true);
                    Pagos.txtIndicador.setText(Identificador);
                    Pagos.txtPago.setText(Precio);
                    Pagos.txtRecibido.setText(Precio);
                    Pagos.txtRecibido.setEnabled(false);
                    Pagos.txtTarjeta.setText(String.valueOf(ID));
                    Pagos.txtFactura.setText(Factura);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Tarjeta Denegada");
                }
            }
            catch(Exception e){
                System.out.println(e);
            }
    }//GEN-LAST:event_btnAceptarActionPerformed

