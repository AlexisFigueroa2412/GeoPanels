    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String Nombre = fac.EncriptarPalabra(txtNombre.getText());
        String Apellido = fac.EncriptarPalabra(txtApellido.getText());
        String PAN = fac.EncriptarPalabra(txtPAN.getText());
        String CVV = fac.EncriptarPalabra(txtCVV.getText());
        String Vencimiento = fac.EncriptarPalabra(txtVencimiento.getText());
        
        fac.setNombreTarjeta(Nombre);
        fac.setApellidoTarjeta(Apellido);
        fac.setPan(PAN);
        fac.setCVV(CVV);
        fac.setVencimiento(Vencimiento);
       
        Obtener_Tarjeta();
        
        if (!txtIdTarjeta.getText().isEmpty()) {
            
            Pagos pg = new Pagos();            
            pg.setVisible(true);
            Pagos.txtIndicador.setText(Indicador);
            Pagos.txtPago.setText(Precio);
            Pagos.txtRecibido.setText(Precio);
            Pagos.txtRecibido.setEnabled(false);
            Pagos.txtTarjeta.setText(txtIdTarjeta.getText());
            Pagos.txtFactura.setText(Factura);
            
            
            this.dispose();
            
        } else {
            NuevaTarjeta nt = new NuevaTarjeta();
            nt.setVisible(true);
            NuevaTarjeta.Nombre = Nombre;
            NuevaTarjeta.Apellido = Apellido;
            NuevaTarjeta.Pan = PAN;
            NuevaTarjeta.CVV = CVV;
            NuevaTarjeta.vencimiento = Vencimiento;
            NuevaTarjeta.Factura = Factura;
            NuevaTarjeta.Identificador = Indicador;
            NuevaTarjeta.Precio = Precio;
            this.dispose();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

    public void Obtener_Tarjeta(){
        try{
            retorno = fac.Obtener_Tarjeta();
            while(retorno.next())
            {
                txtIdTarjeta.setText(retorno.getString(1));
            }
        }
        catch(SQLException e){
            System.out.println(e);
        }
    }
    
