 public void cargarValores() {
     
       DefaultTableModel modelo = (DefaultTableModel) TablaMantenimiento.getModel();
       modelo.setRowCount(0);
       
       Reset = Clases.Conexion.Consulta("Select * from Tipo_mantenimiento");
       try{
       
           while(Reset.next()){
             
               Vector valor = new Vector();
               valor.add(Reset.getInt(1));
               valor.add(Reset.getString(2));
               valor.add(Reset.getInt(3));
               modelo.addRow(valor);
               TablaMantenimiento.setModel(modelo);
               
               
               TablaMantenimiento.getColumnModel().getColumn(0).setMaxWidth(0);
               TablaMantenimiento.getColumnModel().getColumn(0).setMinWidth(0);
               TablaMantenimiento.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
               TablaMantenimiento.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
           }
       }
       catch(Exception e){
       
           System.out.println(e.getMessage());
       }
          }
    private void TablaMantenimientoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMantenimientoMouseClicked
        int col = TablaMantenimiento.getSelectedRow();
        txtId.setText(TablaMantenimiento.getModel().getValueAt(col, 0).toString());
        txtTipoMan.setText(TablaMantenimiento.getModel().getValueAt(col, 1).toString());
        txtCosto.setText(TablaMantenimiento.getModel().getValueAt(col, 2).toString());
    }//GEN-LAST:event_TablaMantenimientoMouseClicked
public void Busqueda(){
    
     String valor = txtTipoMan.getText();
        DefaultTableModel modelo = (DefaultTableModel) TablaMantenimiento.getModel();
        modelo.setRowCount(0);

        //Realizando consulta.
        Reset = Clases.Conexion.Consulta("select * from Tipo_mantenimiento where tipo_mantenimiento = '"+valor+"'");

        try{

            while(Reset.next()){

                //Agregando datos a la tabla.
                Vector datos = new Vector();
                datos.add(Reset.getInt(1));
                datos.add(Reset.getString(2));
                datos.add(Reset.getInt(3));
                modelo.addRow(datos);
                TablaMantenimiento.setModel(modelo);

                //Ocultando la columna 0.
                TablaMantenimiento.getColumnModel().getColumn(0).setMaxWidth(0);
                TablaMantenimiento.getColumnModel().getColumn(0).setMinWidth(0);
                TablaMantenimiento.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
                TablaMantenimiento.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    private void JTBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JTBuscarActionPerformed
        Busqueda();
    }//GEN-LAST:event_JTBuscarActionPerformed

