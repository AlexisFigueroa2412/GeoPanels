    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        //Limitaciones.
        txtPassword.setEnabled(false);
        rbdMasculino.setEnabled(false);
        rbdFemenino.setEnabled(false);
        //Enviando datos de la tabla a los campos.
        int col = TablaDatos.getSelectedRow();
        txtID.setText(TablaDatos.getModel().getValueAt(col, 0).toString());
        txtNombre.setText(TablaDatos.getModel().getValueAt(col, 1).toString());
        txtApellido.setText(TablaDatos.getModel().getValueAt(col, 2).toString());
        txtDireccion.setText(TablaDatos.getModel().getValueAt(col, 3).toString());
        txtTelefono.setText(TablaDatos.getModel().getValueAt(col, 4).toString());
        txtCorreo.setText(TablaDatos.getModel().getValueAt(col, 5).toString());
        txtUsuario.setText(TablaDatos.getModel().getValueAt(col, 6).toString());
        txtPassword.setText(TablaDatos.getModel().getValueAt(col, 7).toString());
        txtNacimiento.setText(TablaDatos.getModel().getValueAt(col, 8).toString());
        String valor = (TablaDatos.getModel().getValueAt(col, 9).toString());

        if (valor.contains("Masculino")) {
            rbdMasculino.setSelected(true);

        }else if (valor.contains("Femenino")) {
            rbdFemenino.setSelected(true);
        }

        String Valor1;//ESTADO.
        String Valor2;//TIPO.
        String Valor3;//ESTABLECIMIENTO.

        funciones.CargarLogo(TablaDatos.getModel().getValueAt(col, 10).toString(), lblFoto);
        Valor1 = TablaDatos.getModel().getValueAt(col, 11).toString();
        Valor2 = TablaDatos.getModel().getValueAt(col, 12).toString();
        Valor3 = TablaDatos.getModel().getValueAt(col, 13).toString();

        //Llamo a la clase del comboBox
        ClaseCombo_Empleados mostrar = new ClaseCombo_Empleados();
        cbxEstado.setSelectedIndex(mostrar.cargarEstado(Valor1));
        cbxTipo.setSelectedIndex(mostrar.cargarTipo(Valor2));
        cbxEstablecimiento.setSelectedIndex(mostrar.cargarEstablecimiento(Valor3));
    }//GEN-LAST:event_TablaDatosMouseClicked

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        //Validando que no se agreguen numeros.

        if (!Character.isLetter(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar()) && !Character.isSpaceChar(evt.getKeyChar()) ) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txtNombreKeyTyped

