    public void cargarValores(){
        DefaultTableModel modelo = (DefaultTableModel) tabla_resultado.getModel();
        modelo.setRowCount(0);
        res = Clases.Conexion.Consulta("Select p.sku, i.fecha_ingreso, i.cantidad, e.nombre_establecimiento From Inventario i, Establecimiento e, Producto p Where i.id_inventariado = e.id_establecimiento and i.id_producto = p.id_producto");
        try{
            while(res.next()){
                Vector valor = new Vector();
                valor.add(res.getInt(1));
                valor.add(res.getString(2));
                valor.add(res.getInt(3));
                valor.add(res.getString(4));
                modelo.addRow(valor);
                tabla_resultado.setModel(modelo);
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
    public void consulta(){
        String sku = (txtSku.getText());
        DefaultTableModel modelo = (DefaultTableModel) tabla_resultado.getModel();
        modelo.setRowCount(0);
        res = Clases.Conexion.Consulta("Select p.sku, i.fecha_ingreso, i.cantidad, e.nombre_establecimiento From Inventario i, Establecimiento e, Producto p Where i.id_inventariado = e.id_establecimiento and i.id_producto = p.id_producto and p.sku = '"+sku+"'");
        try{
            while(res.next()){
                Vector valor = new Vector();
                valor.add(res.getInt(1));
                valor.add(res.getString(2));
                valor.add(res.getInt(3));
                valor.add(res.getString(4));
                modelo.addRow(valor);
                tabla_resultado.setModel(modelo);
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
