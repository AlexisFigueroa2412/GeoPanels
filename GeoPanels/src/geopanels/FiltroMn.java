    public void pasarDatosFila(){
        //Obtenemos los datos
        int filaseleccionada = Tabla.getSelectedRow();
        String identificador = (String)Tabla.getValueAt(filaseleccionada, 0);
        String nombre = (String)Tabla.getValueAt(filaseleccionada, 4);
        String apellido = (String)Tabla.getValueAt (filaseleccionada, 3);
        String costo = (String)Tabla.getValueAt (filaseleccionada,5);
        //Volcamos los datos de la tabla en los jTextFields.
        txtId.setText(identificador);
        txtNombre.setText(nombre);
        txtApellido.setText(apellido);
        txtCosto.setText(costo);
        
    }
    
