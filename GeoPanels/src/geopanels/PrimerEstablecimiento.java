    private void txtSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSiguienteActionPerformed
        //Mediante este botón sera donde se agregara el establecimiento.
        // Tomando los datos del formulario y pasandoles a los atributos de la clase.

       if (txtNombre.getText().equals("") || txtUbicacion.getText().equals("") || txtTelefono.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
        }else{
       
            String Valor1;//MUNICIPIO.
            String Valor2;//EMPRESA.
            String Valor3;//ESTADO.
            
            Valor1 = cbxMunicipio.getItemAt(cbxMunicipio.getSelectedIndex()).getRespuesta();
            Valor2 = cbxEmpresa.getItemAt(cbxEmpresa.getSelectedIndex()).getRespuesta();
            Valor3 = cbxEstado.getItemAt(cbxEstado.getSelectedIndex()).getRespuesta();
            
            ClaseEstablecimiento obj = new ClaseEstablecimiento();
           
            obj.setNombre_establecimiento(txtNombre.getText());
            obj.setUbicacion(txtUbicacion.getText());
            obj.setTelefono(txtTelefono.getText());
            
            //ComboBox
            obj.setId_municipio(Integer.parseInt(Valor1));
            obj.setId_empresa(Integer.parseInt(Valor2));
            obj.setId_estado(Integer.parseInt(Valor3));
            
            if (obj.guardarProyecto()) {
                JOptionPane.showMessageDialog(null, "Los datos ingresados han sido guardados con exito.", "Confirmación", JOptionPane.INFORMATION_MESSAGE);
                txtNombre.setText(null);
                txtUbicacion.setText(null);
                txtTelefono.setText(null);
                
                //Llamando al siguiente formulario.
                PrimerUsuario pru = new PrimerUsuario();
                pru.setVisible(true);
                this.dispose();

            }else{

               JOptionPane.showMessageDialog(null, "Ups ha ocurrido un fallo al momento de guardar los datos, por favor verificar los.", "ERROR", JOptionPane.ERROR_MESSAGE); 
               txtNombre.setText(null);
               txtUbicacion.setText(null);
               txtTelefono.setText(null);
            }
       }  
    }//GEN-LAST:event_txtSiguienteActionPerformed

    //VALIDACIONES DE CAMPOS.
    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        //Validando que no se agreguen puntos ni comas.       
        if (!Character.isLetter(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar()) && !Character.isSpaceChar(evt.getKeyChar()) && !Character.isDigit(evt.getKeyChar()) ) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        } 
    }//GEN-LAST:event_txtNombreKeyTyped

