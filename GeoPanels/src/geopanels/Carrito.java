    private void Llenar_Tabla()
    {
        ClaseCarrito Obj = new ClaseCarrito();
        Obj.setcarrito(txtCarrito.getText());
        DefaultTableModel llenar = (DefaultTableModel) tbCarrito.getModel();
        llenar.setRowCount(0);
        retorno = Obj.Mostrar_carro();
        try
        {
            while(retorno.next())
            {
                Vector v = new Vector();
                v.add(retorno.getString(1));
                v.add(retorno.getString(2));
                v.add(retorno.getString(3));
                v.add(retorno.getString(4));
                v.add(retorno.getString(5));
                llenar.addRow(v);
                tbCarrito.setModel(llenar);
            }
        }
        catch(Exception e)
        {
            System.out.println(e.toString());
        }      
    }
