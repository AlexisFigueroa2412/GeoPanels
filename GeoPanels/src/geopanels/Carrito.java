    public void Limpiar_tabla(){
        DefaultTableModel tb = (DefaultTableModel) tbCarrito.getModel();
        int a = tbCarrito.getRowCount()-1;
        for (int i = a; i >= 0; i--) {          
        tb.removeRow(tb.getRowCount()-1);
        }
    }    
