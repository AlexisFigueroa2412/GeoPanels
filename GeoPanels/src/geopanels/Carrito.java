    private void spnCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_spnCantidadKeyTyped
        // TODO add your handling code here:
        if(spnCantidad.getText().length()>= Máximo.getText().length()){
                evt.consume(); //hace que esa pulsación de tecla se rechace.
                Toolkit.getDefaultToolkit().beep(); //sonido de no aceptar más caracteres.
            }
         if (!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar())) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
        
        
    }//GEN-LAST:event_spnCantidadKeyTyped


