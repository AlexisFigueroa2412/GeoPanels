    private void btnCrearCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearCarritoActionPerformed
        // TODO add your handling code here:
        Date objDate = new Date(); 
        String strDateFormat = "dd-MMM-yyyy-HH-mm";         
        SimpleDateFormat objSDF = new SimpleDateFormat(strDateFormat);    
   
        if (txtCarrito.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Asegurate de no dejar campos vacíos");
        }
        else {
            
            String nombreCarro = txtCarrito.getText();
            //System.out.println(objSDF.format(objDate));
            
            String concatenacion = nombreCarro+"-"+objSDF.format(objDate);
            car.setcarrito(concatenacion);            
            try {
                JOptionPane.showMessageDialog(this, "Carrito "+ car.Getcarrito());
                if (car.CrearCarrito()) {
                    
                    JOptionPane.showMessageDialog(this, "Datos guardados");
                    spnCantidad.setEnabled(true);
                    btnInventario.setEnabled(true);
                    btnCrearCarrito.setEnabled(false);
                    btnLimpiar.setEnabled(true);
                    txtCarrito.setText(concatenacion);
                    txtCarrito.setEnabled(false);
                    Conseguir_Carrito();
                    Llenar_Tabla();    
                    
                }
                else{
                    JOptionPane.showMessageDialog(this, "Error al guardar datos");
                }
            } 
            catch (HeadlessException e) 
            {
                Logger.getLogger(Carrito.class.getName()).log(Level.SEVERE, null, e);
            }
        }
        
    }//GEN-LAST:event_btnCrearCarritoActionPerformed

