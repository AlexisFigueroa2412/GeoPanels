    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
       // Tomando los datos del formulario y pasandoles a los atributos de la clase.

       if (txtNombre.getText().equals("") || txtUbicacion.getText().equals("") || txtTelefono.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
        }else{
       
            String Valor1;//MUNICIPIO.
            String Valor2;//EMPRESA.
            String Valor3;//ESTADO.
            
            Valor1 = cbxMunicipio.getItemAt(cbxMunicipio.getSelectedIndex()).getRespuesta();
            Valor2 = cbxEmpresa.getItemAt(cbxEmpresa.getSelectedIndex()).getRespuesta();          
            Valor3 = cbxEstado.getItemAt(cbxEstado.getSelectedIndex()).getRespuesta();
            
            ClaseEstablecimiento obj = new ClaseEstablecimiento();
           
            obj.setId_establecimiento(Integer.parseInt(txtID.getText()));
            obj.setNombre_establecimiento(txtNombre.getText());
            obj.setUbicacion(txtUbicacion.getText());
            obj.setTelefono(txtTelefono.getText());
            
            //ComboBox
            obj.setId_municipio(Integer.parseInt(Valor1));
            obj.setId_empresa(Integer.parseInt(Valor2));
            obj.setId_estado(Integer.parseInt(Valor3));
            
            if (obj.modificarProyecto()) {
                JOptionPane.showMessageDialog(null, "Datos Modificados correctamente");
                txtNombre.setText(null);
                txtUbicacion.setText(null);
                txtTelefono.setText(null);
                cargarEstablecimientos();

            }else{

               JOptionPane.showMessageDialog(null, "Error al modificar los datos"); 
               txtNombre.setText(null);
               txtUbicacion.setText(null);
               txtTelefono.setText(null);
            }
       }    
    }//GEN-LAST:event_btnActualizarActionPerformed

