    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
         // Llevandose los datos a los campos.
        int col = TablaDatos.getSelectedRow();
        txtID.setText(TablaDatos.getModel().getValueAt(col, 0).toString());
        txtNombre.setText(TablaDatos.getModel().getValueAt(col, 1).toString());
        txtUbicacion.setText(TablaDatos.getModel().getValueAt(col, 2).toString());
        txtTelefono.setText(TablaDatos.getModel().getValueAt(col, 3).toString());
        
        String Valor1;//MUNICIPIO.
        String Valor2;//EMPRESA.
        String Valor3;//ESTADO.
        
        Valor1 = TablaDatos.getModel().getValueAt(col, 4).toString();
        Valor2 = TablaDatos.getModel().getValueAt(col, 5).toString();
        Valor3 = TablaDatos.getModel().getValueAt(col, 6).toString();
        //Llamo a la clase del comboBox
        ClaseCombo_Establecimiento mostrar = new ClaseCombo_Establecimiento();
        cbxMunicipio.setSelectedIndex(mostrar.cargarMunicipio(Valor1));
        cbxEmpresa.setSelectedIndex(mostrar.cargarEmpresa(Valor2));
        cbxEstado.setSelectedIndex(mostrar.cargarEstado(Valor3));
    }//GEN-LAST:event_TablaDatosMouseClicked

