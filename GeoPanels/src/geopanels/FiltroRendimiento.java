public void cargarValores(){
        DefaultTableModel modelo = (DefaultTableModel) TablaRendimiento.getModel();
        modelo.setRowCount(0);
        res = Clases.Conexion.Consulta("Select p.id_produccion, d.nombre_producto, p.produccion from produccion p, producto d where p.id_producto = d.id_producto");
        try{
            while(res.next()){
                Vector valor = new Vector();
                valor.add(res.getInt(1));
                valor.add(res.getString(2));
                valor.add(res.getString(3));

                
         
                modelo.addRow(valor);
                TablaRendimiento.setModel(modelo);

                TablaRendimiento.getColumnModel().getColumn(0).setMaxWidth(0);
                TablaRendimiento.getColumnModel().getColumn(0).setMinWidth(0);
                TablaRendimiento.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
                TablaRendimiento.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
