    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (!txtIdCarro.getText().isEmpty() && !txtNombre.getText().isEmpty()) {

            Compra.txtIdCarrito.setText(this.txtIdCarro.getText());
            Compra.txtCarrito.setText(this.txtNombre.getText());

            fac.setCarrito(txtNombre.getText());
            retorno = fac.Obtener_Precio();
            //            JOptionPane.showMessageDialog(this, "muestra los"+fac.getCarrito());

            try{
                while(retorno.next())
                {
                    Compra.txtCantidad.setText(retorno.getString(1));
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "No se pudo obtener el id perteneciente al carrito" +e);
            }
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione el proveedor");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

