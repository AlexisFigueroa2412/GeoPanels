    //Cargando los datos en la tabla.
    public void cargarMunicipios(){

        DefaultTableModel modelo = (DefaultTableModel) TablaDatos.getModel();
        modelo.setRowCount(0);

        //Realizando consulta.
        result = Clases.Conexion.Consulta("Select m.id_municipio, m.municipio, d.departamento from Municipio m, Departamentos d where m.id_departamento = d.id_departamento");

        try{

            while(result.next()){

                //Agregando datos a la tabla.
                Vector datos = new Vector();
                datos.add(result.getInt(1));
                datos.add(result.getString(2));
                datos.add(result.getString(3));
                modelo.addRow(datos);
                TablaDatos.setModel(modelo);

                //Ocultando la columna 0.
                TablaDatos.getColumnModel().getColumn(0).setMaxWidth(0);
                TablaDatos.getColumnModel().getColumn(0).setMinWidth(0);
                TablaDatos.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
                TablaDatos.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
    //Creando la función para la busqueda.
    public void busqueda(){

        String valor = txtMunicipio.getText();
        DefaultTableModel modelo = (DefaultTableModel) TablaDatos.getModel();
        modelo.setRowCount(0);

        //Realizando consulta.
        result = Clases.Conexion.Consulta("Select m.id_municipio, m.municipio, d.departamento from Municipio m, Departamentos d where m.id_departamento = d.id_departamento and m.municipio = '"+valor+"'");

        try{

            while(result.next()){

                //Agregando datos a la tabla.
                Vector datos = new Vector();
                datos.add(result.getInt(1));
                datos.add(result.getString(2));
                datos.add(result.getString(3));
                modelo.addRow(datos);
                TablaDatos.setModel(modelo);

                //Ocultando la columna 0.
                TablaDatos.getColumnModel().getColumn(0).setMaxWidth(0);
                TablaDatos.getColumnModel().getColumn(0).setMinWidth(0);
                TablaDatos.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
                TablaDatos.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
