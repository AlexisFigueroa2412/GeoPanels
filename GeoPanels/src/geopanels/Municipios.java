    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
        // Llevandose los datos a los campos.
        int col = TablaDatos.getSelectedRow();
        txtID.setText(TablaDatos.getModel().getValueAt(col, 0).toString());
        txtMunicipio.setText(TablaDatos.getModel().getValueAt(col, 1).toString());
        
        String valor;
        valor = TablaDatos.getModel().getValueAt(col, 2).toString();
        //Llamo a la clase del comboBox
        ClaseCombo_Municipio mostrar = new ClaseCombo_Municipio();
        cbxDepartamento.setSelectedIndex(mostrar.cargarDepartamento(valor));
        
    }//GEN-LAST:event_TablaDatosMouseClicked

