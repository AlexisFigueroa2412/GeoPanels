    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // Tomando los datos del formulario y pasandoles a los atributos de la clase.

       if (txtMunicipio.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
        }else{
       
            String valor;
            valor = cbxDepartamento.getItemAt(cbxDepartamento.getSelectedIndex()).getRespuesta();
            ClaseMunicipio obj = new ClaseMunicipio();
           
            obj.setMunicipio(txtMunicipio.getText());
            obj.setId_departamento(Integer.parseInt(valor));
            
            if (obj.guardarProyecto()) {
                JOptionPane.showMessageDialog(null, "Datos Guardados");
                txtMunicipio.setText(null);
                cargarMunicipios();

            }else{

               JOptionPane.showMessageDialog(null, "Error al guardar datos"); 
               txtMunicipio.setText(null);
            }
       }  
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // Tomando los datos del formulario y pasandoles a los atributos de la clase.

        if (txtMunicipio.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Debes completar todos los campos necesarios para continuar el proceso con normalidad.", "ADVERTENCIA", JOptionPane.INFORMATION_MESSAGE);
        }else{
        
           String valor;
           valor = cbxDepartamento.getItemAt(cbxDepartamento.getSelectedIndex()).getRespuesta();
           ClaseMunicipio obj = new ClaseMunicipio();

           obj.setId_municipio(Integer.parseInt(txtID.getText()));
           obj.setMunicipio(txtMunicipio.getText());
           obj.setId_departamento(Integer.parseInt(valor));

           if (obj.modificarProyecto()) {
               JOptionPane.showMessageDialog(null, "Datos Modificados correctamente");
               txtMunicipio.setText(null);
               cargarMunicipios();

           }else{

              JOptionPane.showMessageDialog(null, "Error al modificar los datos"); 
              txtMunicipio.setText(null);
           }
      }
    }//GEN-LAST:event_btnActualizarActionPerformed

